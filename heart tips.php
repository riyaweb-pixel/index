    <h2>Tips for a Healthy Heart</h2>

    <p>
        A healthy heart is essential for a long and active life. Heart disease is one
        of the leading causes of health problems, but the good news is that it can be
        prevented with simple lifestyle changes and regular medical care.
    </p>

    <h3>Eat a Heart-Healthy Diet</h3>
    <p>
        Choose foods that are low in saturated fats, salt, and sugar. Include fruits,
        vegetables, whole grains, nuts, and lean proteins in your daily diet.
    </p>

    <h3>Exercise Regularly</h3>
    <p>
        Physical activity helps strengthen the heart and improves blood circulation.
        Aim for at least 30 minutes of moderate exercise, such as walking or cycling,
        five days a week.
    </p>

    <h3>Maintain a Healthy Weight</h3>
    <p>
        Being overweight increases the risk of heart disease. Maintaining a healthy
        body weight helps reduce strain on the heart and lowers cholesterol levels.
    </p>

    <h3>Quit Smoking and Limit Alcohol</h3>
    <p>
        Smoking damages blood vessels and increases the risk of heart attacks. Limiting
        alcohol intake also helps keep your heart healthy.
    </p>

    <h3>Manage Stress</h3>
    <p>
        Chronic stress can negatively affect heart health. Practice relaxation
        techniques such as meditation, yoga, and deep breathing exercises.
    </p>

    <h3>Get Regular Health Checkups</h3>
    <p>
        Regular checkups help monitor blood pressure, cholesterol, and blood sugar
        levels. Early detection of heart-related issues can prevent serious problems.
    </p>

